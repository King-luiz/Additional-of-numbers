public class Sum {
    public static void main(String[] args) {
        // Declare and initialize three integer variables with given values
        int num1 = 10;  // First number
        int num2 = 20;  // Second number
        int num3 = 30;  // Third number

        // Calculate the sum of num1, num2, and num3
        int sum = num1 + num2 + num3;

        // Display the sum to the console
        System.out.println("The sum is: " + sum);
    }
}
